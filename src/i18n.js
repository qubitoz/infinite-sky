// Tiny i18n layer. `t(key)` for fixed UI strings, `pick(obj)` for dual-language
// data objects ({en, es}) coming from generated content. Language persists per
// device and can be toggled any time with X or the title-screen button.
let stored = null;
try { stored = localStorage.getItem('infsky-lang'); } catch { /* fine */ }
export let lang = stored || ((navigator.language || 'en').toLowerCase().startsWith('es') ? 'es' : 'en');

const listeners = [];
export function onLang(fn) { listeners.push(fn); }
export function setLang(l) {
  lang = l;
  try { localStorage.setItem('infsky-lang', l); } catch { /* fine */ }
  for (const fn of listeners) fn(l);
}
export function toggleLang() { setLang(lang === 'en' ? 'es' : 'en'); }

export const pick = (d) => (!d ? '' : (typeof d === 'string' ? d : (d[lang] ?? d.en)));
export const t = (k) => { const e = DICT[k]; return e ? (e[lang] ?? e.en) : k; };

